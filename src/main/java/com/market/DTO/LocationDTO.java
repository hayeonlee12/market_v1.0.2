package com.market.DTO;

public class LocationDTO {
	private String locationid;
	private String userid;
	private String locaddr;
	private String locname;
	private String locphone;
	private String locbasic;
	
	public String getLocationid() {
		return locationid;
	}
	public void setLocationid(String locationid) {
		this.locationid = locationid;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getLocaddr() {
		return locaddr;
	}
	public void setLocaddr(String locaddr) {
		this.locaddr = locaddr;
	}
	public String getLocname() {
		return locname;
	}
	public void setLocname(String locname) {
		this.locname = locname;
	}
	public String getLocphone() {
		return locphone;
	}
	public void setLocphone(String locphone) {
		this.locphone = locphone;
	}
	public String getLocbasic() {
		return locbasic;
	}
	public void setLocbasic(String locbasic) {
		this.locbasic = locbasic;
	}
	
	
	
}
